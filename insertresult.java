    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rollno=jTextField1.getText();
        String physics=jTextField2.getText();
        String EM=jTextField3.getText();
        String DBMS=jTextField4.getText();
        String maths=jTextField5.getText();
                try{
                    Class.forName("com.mysql.cj.jdbc.Driver");
                    Connection con=(Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Mayur@27");
                    
                    Statement st=con.createStatement();
                    
                    ResultSet rs=st.executeQuery("select * from student where rollno='"+rollno+"'");
                    if(rs.next()){
                        st.executeUpdate("insert into result(rollno,physics,maths,DBMS,EM)values('"+rollno+"','"+physics+"','"+maths+"','"+DBMS+"','"+EM+"')");
                        JOptionPane.showMessageDialog(null,"Result is saved Successfully");
                        setVisible(false);
                        new insertresult().setVisible(true);
                    }
                    else{
                        JOptionPane.showMessageDialog(null,"Student is registered!");               
                    }
                }
                catch(Exception e){
                    JOptionPane.showMessageDialog(null,e.toString());
                }
    }//GEN-LAST:event_jButton1ActionPerformed

