    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        try{
            String rollno=jTextField3.getText();
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Mayur@27");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from result inner join student where student.rollno='"+rollno+"' and result.rollno='"+rollno+"'");
            
            if(rs.next()){
                jTextField1.setText(rs.getString(7));
                jTextField2.setText(rs.getString(10));
                jTextField4.setText(rs.getString(8));
                jTextField5.setText(rs.getString(9));
               
                
                jTextField6.setText(rs.getString(2));
                jTextField7.setText(rs.getString(3));
                jTextField8.setText(rs.getString(4));
                jTextField9.setText(rs.getString(5));
                
                int s1=Integer.parseInt(jTextField6.getText());
                int s2=Integer.parseInt(jTextField7.getText());
                int s3=Integer.parseInt(jTextField8.getText());
                int s4=Integer.parseInt(jTextField9.getText());
                
                int result=s1+s2+s3+s4;
                int avg=result/4;
                jTextField10.setText(String.valueOf(result));
                
                if(avg>=45){
                  jTextField11.setText("PASS");
                }else{
                  jTextField11.setText("FAIL");
                }
                
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(studenthome.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(studenthome.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formComponentShown

